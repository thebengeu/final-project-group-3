#import "UIColor+Channely.h"

@implementation UIColor (Channely)

+ (UIColor *)channelyGray
{
    return [UIColor colorWithWhite:0.75f alpha:1.0f];
}

+ (UIColor *)channelyRed
{
    return [UIColor colorWithRed:0.792 green:0.031 blue:0.078 alpha:1];
}

@end
